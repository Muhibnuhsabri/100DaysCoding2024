package coding100day;

public class Day_70 {
    
    public static int bukudanpulpen(int buku, int pulpen) {
        
        return buku+pulpen;
                    
    }
    
    public static void main(String[] args) {
        
        int buku = 5;
        int pulpen = 2;
        
        int hasil = bukudanpulpen (buku,pulpen);
        
        System.out.println("buku dan pulpen saya ada = "+hasil);
        
    }
        
}
