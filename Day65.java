package coding100day;

public class Day_65 {
    
    public static void main(String[] args) {
        
        System.out.println("perkalian 1 sampai 10 ");
        
        for (int i = 1; i <= 10; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.print(i +" kali "+j+" = " + i*j + "\t");
                
            }System.out.println("");
        }
        
    }
    
}
